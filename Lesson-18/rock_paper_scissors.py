# Игра: Камень, ножницы, бумага
#
# Создайте игру "Камень, ножницы, бумага", где пользователь играет против компьютера.
# Компьютер случайным образом выбирает одно из трех значений: камень, ножницы или бумагу.
# Пользователь вводит свой выбор, и программа определяет победителя. Если выборы одинаковы, игра объявляет ничью.
# Игра продолжается до тех пор, пока один из игроков (пользователь или компьютер)
# не одержит на три победы больше, чем соперник. В конце показывается итоговый счет и объясляется победитель.

import random


def game():
    options = ["камень", "ножницы", "бумага"]
    user_score = 0
    computer_score = 0

    print("Добро пожаловать в игру 'Камень, ножницы, бумага'!")
    print("Цель: Набрать на 3 победы больше, чем соперник.\n")

    while (user_score - computer_score) < 3:
        # Компьютер выбирает случайный вариант
        computer_choice = random.choice(options)
        # Игрок вводит свой выбор
        user_choice = input("Ваш выбор (камень, ножницы, бумага): ").lower()

        # Проверка, что выбор корректен
        if user_choice not in options:
            print("Некорректный выбор. Попробуйте снова.")
            continue

        print(f"Компьютер выбрал: {computer_choice}")

        # Определение результата
        if user_choice == computer_choice:
            print("Ничья!")
        elif (user_choice == "камень" and computer_choice == "ножницы") or \
                (user_choice == "ножницы" and computer_choice == "бумага") or \
                (user_choice == "бумага" and computer_choice == "камень"):
            user_score += 1
            print("Вы выиграли раунд!")
        else:
            computer_score += 1
            print("Компьютер выиграл раунд!")

        print(f"Счет: Вы - {user_score}, Компьютер - {computer_score}\n")

    # Определяем победителя игры
    if user_score > computer_score:
        print("Поздравляем! Вы победили!")
    else:
        print("Компьютер выиграл игру.")

    print(f"Итоговый счет: Вы - {user_score}, Компьютер - {computer_score}")


game()